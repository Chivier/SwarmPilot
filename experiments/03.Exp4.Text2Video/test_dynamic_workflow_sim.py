class A2TaskReceiver:
    """
    Thread 3: Receive A2 results from Scheduler A and submit B tasks.
    
    When an A2 task completes:
    1. Extract negative prompt from result
    2. Submit corresponding B task to Scheduler B
    """

    def __init__(self,
                 scheduler_b_url: str,
                 ws_url: str,
                 a2_task_ids: List[str],
                 workflow_states: Dict[str, WorkflowState],
                 task_records: Dict[str, TaskRecord],
                 b_tasks: Dict[str, WorkflowTaskData],
                 model_id: str = "t2vid",
                 mode: str = "simulation",
                 rate_limiter: Optional[RateLimiter] = None):
        """
        Initialize A2 task receiver.

        Args:
            scheduler_b_url: Scheduler B URL (for B task submission)
            ws_url: WebSocket URL for Scheduler A (for A2 results)
            a2_task_ids: List of A2 task IDs to subscribe to
            workflow_states: Shared workflow state dictionary
            task_records: Shared task record dictionary
            b_tasks: Pre-generated B task data (keyed by workflow_id)
            model_id: Model ID for B tasks
            mode: "simulation" or "real"
            rate_limiter: Optional shared rate limiter
        """
        self.scheduler_b_url = scheduler_b_url
        self.ws_url = ws_url
        self.a2_task_ids = a2_task_ids
        self.workflow_states = workflow_states
        self.task_records = task_records
        self.b_tasks = b_tasks
        self.model_id = model_id
        self.mode = mode
        self.rate_limiter = rate_limiter
        self.logger = logging.getLogger("Thread3.A2TaskReceiver")

        # Tracking
        self.completed_a2_tasks = 0
        self.failed_a2_tasks = 0
        self.submitted_b_tasks = 0
        self.failed_b_submissions = 0

        # Thread control
        self.thread: Optional[threading.Thread] = None
        self.running = False
        self.loop: Optional[asyncio.AbstractEventLoop] = None

    async def _run_async(self):
        """Main async loop for receiving A2 results."""
        self.logger.info(f"Connecting to Scheduler A WebSocket for A2 results: {self.ws_url}")

        try:
            async with websockets.connect(
                self.ws_url,
                ping_interval=20,  # Send ping every 20 seconds
                ping_timeout=10,   # Wait up to 10 seconds for pong
                close_timeout=10   # Wait up to 10 seconds for close handshake
            ) as websocket:
                # Subscribe to A2 task IDs
                subscribe_msg = {
                    "type": "subscribe",
                    "task_ids": self.a2_task_ids
                }
                await websocket.send(json.dumps(subscribe_msg))
                self.logger.info(f"Sent subscription request for {len(self.a2_task_ids)} A2 tasks")

                # Wait for acknowledgment
                try:
                    ack = await asyncio.wait_for(websocket.recv(), timeout=5.0)
                    ack_data = json.loads(ack)
                    if ack_data.get("type") == "ack":
                        self.logger.info(f"Subscription confirmed for {len(ack_data.get('subscribed_tasks', []))} A2 tasks")
                    else:
                        self.logger.warning(f"Unexpected acknowledgment: {ack_data}")
                except asyncio.TimeoutError:
                    self.logger.error("Timeout waiting for subscription acknowledgment")
                    return

                # Receive loop
                while self.running:
                    try:
                        message = await asyncio.wait_for(websocket.recv(), timeout=1.0)
                        data = json.loads(message)

                        # Handle different message types
                        if data.get("type") == "ack":
                            # Skip acknowledgment messages
                            continue
                        elif data.get("type") == "result":
                            # Process A2 task completion
                            await self._handle_a2_result(data)
                        elif data.get("type") == "error":
                            self.logger.error(f"WebSocket error: {data.get('error')}")
                        # Ignore other message types (ping, pong, etc.)

                    except asyncio.TimeoutError:
                        continue
                    except websockets.ConnectionClosed:
                        self.logger.warning("WebSocket connection closed")
                        break
                    except Exception as e:
                        self.logger.error(f"Error receiving A2 result: {e}")

        except Exception as e:
            self.logger.error(f"WebSocket connection error: {e}")

    async def _handle_a2_result(self, data: Dict):
        """Handle A2 task completion and submit B task."""
        task_id = data.get("task_id")
        metadata = data.get("metadata", {})
        workflow_id = metadata.get("workflow_id") or data.get("workflow_id")

        # If workflow_id not in metadata, extract from task_id
        # Format: task-A2-{strategy}-workflow-{i:04d}
        if not workflow_id and task_id:
            parts = task_id.split("-")
            if len(parts) >= 5 and parts[3] == "workflow":
                workflow_id = f"{parts[2]}-workflow-{parts[4]}"

        status = data.get("status")
        result = data.get("result", {})
        execution_time_ms = data.get("execution_time_ms")

        complete_time = time.time()
        
        # Update task record
        if task_id in self.task_records:
            task_record = self.task_records[task_id]
            task_record.complete_time = complete_time
            task_record.status = status
            task_record.execution_time_ms = execution_time_ms
            task_record.result = result
        
        # Update workflow state
        workflow_state = self.workflow_states.get(workflow_id)
        if not workflow_state:
            self.logger.error(f"Workflow state not found for {workflow_id}")
            return
        
        # Check task status
        if status != "completed":
            self.logger.warning(f"A2 task {task_id} failed with status: {status}")
            self.failed_a2_tasks += 1
            return
        
        # Mark A2 as complete
        workflow_state.mark_a2_complete(complete_time)
        self.completed_a2_tasks += 1
        
        # Extract negative prompt from result
        if self.mode == "simulation":
            negative_prompt = result.get("output", "simulated negative prompt")
        else:
            negative_prompt = result.get("output", "")
        
        # Submit B task
        await self._submit_b_task(workflow_id, negative_prompt)

    async def _submit_b_task(self, workflow_id: str, negative_prompt: str):
        """Submit initial B task (B1) to Scheduler B."""
        b_config = self.b_tasks.get(workflow_id)
        if not b_config:
            self.logger.error(f"B task config not found for workflow {workflow_id}")
            return

        workflow_state = self.workflow_states.get(workflow_id)
        if not workflow_state:
            self.logger.error(f"Workflow state not found for {workflow_id}")
            return

        # Get first B task data
        b_task_data = b_config["initial_task"]

        # Apply global rate limiting if configured
        if self.rate_limiter:
            loop = asyncio.get_event_loop()
            await loop.run_in_executor(None, self.rate_limiter.acquire)

        # Build task_input based on mode
        if self.mode == "simulation":
            task_input = {"sleep_time": b_task_data.sleep_time}
        else:  # real mode
            task_input = {
                "positive_prompt": b_task_data.positive_prompt or "",
                "negative_prompt": negative_prompt,
                "frame_count": b_task_data.frame_count or 16,
                "iteration": 1  # First iteration
            }

        # Build metadata
        if self.mode == "simulation":
            metadata = {
                "workflow_id": b_task_data.workflow_id,
                "exp_runtime": b_task_data.exp_runtime,
                "frame_count": b_task_data.frame_count,
                "task_type": "B",
                "b_iteration": 1,
                "max_b_loops": workflow_state.max_b_loops
            }
        else:
            metadata = {
                "workflow_id": b_task_data.workflow_id,
                "positive_prompt": b_task_data.positive_prompt,
                "negative_prompt": negative_prompt,
                "frame_count": b_task_data.frame_count,
                "task_type": "B",
                "b_iteration": 1,
                "max_b_loops": workflow_state.max_b_loops
            }

        payload = {
            "task_id": b_task_data.task_id,
            "model_id": self.model_id,
            "task_input": task_input,
            "metadata": metadata
        }

        # Set submit_time BEFORE attempting submission
        submit_time = time.time()

        try:
            # Submit via POST (run in executor to avoid blocking)
            loop = asyncio.get_event_loop()
            response = await loop.run_in_executor(
                None,
                lambda: requests.post(
                    f"{self.scheduler_b_url}/task/submit",
                    json=payload,
                    timeout=5.0
                )
            )
            response.raise_for_status()
            result = response.json()

            # Extract assigned_instance
            assigned_instance = result.get("assigned_instance")

            # Create task record
            task_record = TaskRecord(
                task_id=b_task_data.task_id,
                workflow_id=workflow_id,
                task_type="B",
                sleep_time=b_task_data.sleep_time,
                exp_runtime=b_task_data.exp_runtime,
                submit_time=submit_time,
                assigned_instance=assigned_instance,
                is_warmup=b_task_data.is_warmup
            )

            # Store task record
            self.task_records[b_task_data.task_id] = task_record

            # Update workflow state - use list append for B loop support
            workflow_state.b_submit_times.append(submit_time)
            workflow_state.b_loop_count = 1  # First iteration

            self.submitted_b_tasks += 1

        except Exception as e:
            self.logger.error(f"Failed to submit B task {b_task_data.task_id}: {e}")
            self.failed_b_submissions += 1

    def _run(self):
        """Thread entry point."""
        self.loop = asyncio.new_event_loop()
        asyncio.set_event_loop(self.loop)
        self.loop.run_until_complete(self._run_async())

    def start(self):
        """Start the receiver thread."""
        if self.running:
            self.logger.warning("A2 task receiver already running")
            return
        
        self.running = True
        self.thread = threading.Thread(target=self._run, daemon=True)
        self.thread.start()
        self.logger.info("A2 task receiver thread started")

    def stop(self):
        """Stop the receiver thread."""
        if not self.running:
            return
        
        self.logger.info("Stopping A2 task receiver...")
        self.running = False

    def wait(self, timeout: Optional[float] = None):
        """Wait for receiver thread to complete."""
        if self.thread:
            self.thread.join(timeout=timeout)


