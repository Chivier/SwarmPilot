def main():
    """Main entry point."""
    parser = argparse.ArgumentParser(
        description="Experiment 03: Text2Video Workflow (A1→A2→B)",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter
    )

    parser.add_argument("--num-workflows", type=int, default=24,
                       help="Number of workflows to execute per strategy")
    parser.add_argument("--qps", type=float, default=6.0,
                       help="Target QPS for A1 task submission")
    parser.add_argument("--seed", type=int, default=42,
                       help="Random seed for reproducibility")
    parser.add_argument("--strategies", nargs="+",
                       default=["probabilistic", "round_robin", "min_time", "random", "po2"],
                       choices=["min_time", "round_robin", "probabilistic", "random", "po2"],
                       help="Scheduling strategies to test")
    parser.add_argument("--mode", type=str, default="simulation",
                       choices=["simulation", "real"],
                       help="Test mode: simulation (sleep) or real (actual models)")
    parser.add_argument("--model-a-id", type=str, default="sleep_model_a",
                       help="Model ID for A1/A2 tasks (default: sleep_model_a for simulation)")
    parser.add_argument("--model-b-id", type=str, default="sleep_model_b",
                       help="Model ID for B tasks (default: sleep_model_b for simulation)")
    parser.add_argument("--gqps", type=float, default=None,
                       help="Global QPS limit for all task submissions")
    parser.add_argument("--warmup", type=float, default=0.0,
                       help="Warmup task ratio (0.0-1.0)")
    parser.add_argument("--continuous", action="store_true",
                       help="Enable continuous request mode")
    parser.add_argument("--timeout", type=int, default=30,
                       help="Test timeout in minutes")
    parser.add_argument("--output-dir", type=Path, default=Path("results"),
                       help="Output directory for results")
    parser.add_argument("--metric-portion", type=float, default=0.5,
                       help="Portion of earliest submitted workflows to use for metric calculation (0.0-1.0, default: 0.5)")

    args = parser.parse_args()

    # Set random seed
    np.random.seed(args.seed)

    # Create output directory
    args.output_dir.mkdir(parents=True, exist_ok=True)

    # Generate workload
    logger = logging.getLogger("Main")
    logger.info("Generating Sleep Model workload...")

    # Determine total workflows needed
    num_warmup = int(args.num_workflows * args.warmup)
    if args.continuous:
        total_workflows = args.num_workflows * 2 + num_warmup
    else:
        total_workflows = args.num_workflows + num_warmup

    workload, config = generate_sleep_model_workload(
        num_workflows=total_workflows,
        seed=args.seed
    )

    print_sleep_model_stats(workload)

    # Run tests for each strategy
    all_results = {}
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")

    for strategy in args.strategies:
        logger.info(f"\n{'='*80}")
        logger.info(f"Testing strategy: {strategy}")
        logger.info(f"{'='*80}")

        # Reset random seed before each strategy to ensure identical task submission patterns
        np.random.seed(args.seed)
        logger.info(f"Reset random seed to {args.seed} for reproducible Poisson intervals")

        try:
            results = test_strategy_workflow(
                strategy=strategy,
                num_workflows=args.num_workflows,
                qps=args.qps,
                workload=workload,
                mode=args.mode,
                model_a_id=args.model_a_id,
                model_b_id=args.model_b_id,
                gqps=args.gqps,
                warmup_ratio=args.warmup,
                continuous_mode=args.continuous,
                timeout_minutes=args.timeout,
                metric_portion=args.metric_portion
            )

            all_results[strategy] = results

            # Save results for this strategy
            output_file = args.output_dir / f"text2video_{strategy}_{timestamp}.json"
            with open(output_file, "w") as f:
                json.dump(results, f, indent=2)
            logger.info(f"Results saved to {output_file}")

        except Exception as e:
            logger.error(f"Error testing strategy {strategy}: {e}", exc_info=True)
            continue

    # Save combined results
    combined_results = {
        "experiment": "03_text2video_workflow",
        "timestamp": timestamp,
        "config": {
            "num_workflows": args.num_workflows,
            "qps": args.qps,
            "gqps": args.gqps,
            "seed": args.seed,
            "strategies": args.strategies,
            "mode": args.mode,
            "model_a_id": args.model_a_id,
            "model_b_id": args.model_b_id,
            "warmup_ratio": args.warmup,
            "continuous_mode": args.continuous,
            "metric_portion": args.metric_portion
        },
        "workload_config": asdict(config),
        "strategies": all_results
    }

    combined_file = args.output_dir / f"text2video_combined_{timestamp}.json"
    with open(combined_file, "w") as f:
        json.dump(combined_results, f, indent=2)
    logger.info(f"Combined results saved to {combined_file}")

    # Print comparison table for all strategies
    if all_results:
        print_strategy_comparison_table(all_results)

    logger.info("\nAll tests completed!")


if __name__ == "__main__":
    main()
