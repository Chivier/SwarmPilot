def print_strategy_comparison_table(all_results: Dict[str, Dict]):
    """
    Print a comprehensive comparison table for all strategies.

    Shows task-level (A1, A2, B) and workflow-level metrics including
    P50, P95, P99 for completion times.

    Args:
        all_results: Dictionary mapping strategy names to their results
    """
    if not all_results:
        print("\nNo results to display.")
        return

    print("\n" + "=" * 120)
    print("STRATEGY COMPARISON - SUMMARY TABLE")
    print("=" * 120)

    # Extract strategy names
    strategies = sorted(all_results.keys())

    # Print Overall Execution Statistics
    print("\n" + "-" * 120)
    print("OVERALL EXECUTION STATISTICS")
    print("-" * 120)
    header = f"{'Strategy':<15} | {'Total Tasks':<12} | {'Total WF':<10} | {'Warmup WF':<10} | {'Target WF':<10} | {'Used WF':<10} | {'Excluded':<10}"
    print(header)
    print("-" * 120)

    for strategy in strategies:
        results = all_results[strategy]
        a1 = results.get("a1_tasks", {})
        a2 = results.get("a2_tasks", {})
        b = results.get("b_tasks", {})
        wf = results.get("workflows", {})

        total_tasks = (a1.get("num_total_completed", 0) +
                      a2.get("num_total_completed", 0) +
                      b.get("num_total_completed", 0))

        print(f"{strategy:<15} | {total_tasks:<12} | "
              f"{wf.get('num_total_workflows', 0):<10} | "
              f"{wf.get('num_warmup', 0):<10} | "
              f"{wf.get('num_total_target', 0):<10} | "
              f"{wf.get('num_completed', 0):<10} | "
              f"{wf.get('num_excluded', 0):<10}")

    # Print Task-Level Metrics (Completion Time Breakdown)
    print("\n" + "-" * 120)
    print("TASK COMPLETION TIME METRICS (seconds) - Based on workflows used for metrics")
    print("-" * 120)

    # Header
    header = f"{'Strategy':<15} | {'Task':<4} | {'Count':<6} | {'P50':>8} | {'P95':>8} | {'P99':>8} | {'Avg':>8} | {'Min':>8} | {'Max':>8}"
    print(header)
    print("-" * 120)

    # Print each strategy's task metrics
    for strategy in strategies:
        results = all_results[strategy]

        # A1 tasks
        a1 = results.get("a1_tasks", {})
        if a1.get("num_completed", 0) > 0:
            print(f"{strategy:<15} | {'A1':<4} | {a1['num_completed']:<6} | "
                  f"{a1.get('median_completion_time', 0):>8.2f} | "
                  f"{a1.get('p95_completion_time', 0):>8.2f} | "
                  f"{a1.get('p99_completion_time', 0):>8.2f} | "
                  f"{a1.get('avg_completion_time', 0):>8.2f} | "
                  f"{a1.get('min_completion_time', 0):>8.2f} | "
                  f"{a1.get('max_completion_time', 0):>8.2f}")

        # A2 tasks
        a2 = results.get("a2_tasks", {})
