class PoissonTaskSubmitter:
    """
    Thread 1: Submit A1 tasks following Poisson process with QPS control.

    Submits tasks to Scheduler A with exponentially distributed inter-arrival
    times to simulate realistic workload patterns.
    """

    def __init__(self,
                 scheduler_url: str,
                 tasks: List[WorkflowTaskData],
                 qps: float,
                 workflow_states: Dict[str, WorkflowState],
                 task_records: Dict[str, TaskRecord],
                 model_id: str = "llm_service_small_model",
                 mode: str = "simulation",
                 rate_limiter: Optional[RateLimiter] = None):
        """
        Initialize Poisson task submitter.

        Args:
            scheduler_url: Scheduler A URL
            tasks: List of pre-generated A1 task data
            qps: Target queries per second
            workflow_states: Shared workflow state dictionary
            task_records: Shared task record dictionary
            model_id: Model ID to use for A1 tasks
            mode: "simulation" or "real"
            rate_limiter: Optional shared rate limiter for global QPS control
        """
        self.scheduler_url = scheduler_url
        self.tasks = tasks
        self.qps = qps
        self.workflow_states = workflow_states
        self.task_records = task_records
        self.model_id = model_id
        self.mode = mode
        self.rate_limiter = rate_limiter
        self.logger = logging.getLogger("Thread1.A1TaskSubmitter")

        # Tracking
        self.submitted_tasks: List[TaskRecord] = []
        self.submission_start_time: Optional[float] = None
        self.submission_end_time: Optional[float] = None
        self.failed_submissions: int = 0

        # Thread control
        self.thread: Optional[threading.Thread] = None
        self.running = False

    def _submit_task(self, task_data: WorkflowTaskData) -> TaskRecord:
        """
        Submit a single A1 task to Scheduler A.

        Args:
            task_data: Pre-generated task data

        Returns:
            TaskRecord with submission info
        """
        # Build task_input based on mode
        if self.mode == "simulation":
            task_input = {"sleep_time": task_data.sleep_time}
        else:  # real mode
            task_input = {
                "sentence": task_data.caption,
                "max_tokens": task_data.max_tokens or 512
            }

        # Build metadata
        if self.mode == "simulation":
            metadata = {
                "workflow_id": task_data.workflow_id,
                "exp_runtime": task_data.exp_runtime,
                "task_type": "A1"
            }
        else:
            # Calculate token_length for prediction
            token_length = estimate_token_length(task_data.caption) if task_data.caption else 0
            max_tokens = task_data.max_tokens or 512

            metadata = {
                "workflow_id": task_data.workflow_id,
                "caption": task_data.caption,
                "token_length": token_length,
                "max_tokens": max_tokens,
                "task_type": "A1"
            }
        
        payload = {
            "task_id": task_data.task_id,
            "model_id": self.model_id,
            "task_input": task_input,
            "metadata": metadata
        }
        
        # Set submit_time BEFORE attempting submission
        submit_time = time.time()
        
        try:
            # Submit via POST
            response = requests.post(
                f"{self.scheduler_url}/task/submit",
                json=payload,
                timeout=5.0
            )
            response.raise_for_status()
            result = response.json()
            
            # Extract assigned_instance from response
            assigned_instance = result.get("assigned_instance")
            
            # Create task record
            task_record = TaskRecord(
                task_id=task_data.task_id,
                workflow_id=task_data.workflow_id,
                task_type="A1",
                sleep_time=task_data.sleep_time,
                exp_runtime=task_data.exp_runtime,
                submit_time=submit_time,
                assigned_instance=assigned_instance,
                is_warmup=task_data.is_warmup
            )

            # Store task record in shared dictionary
            self.task_records[task_data.task_id] = task_record

            # Update workflow state
            workflow_state = self.workflow_states.get(task_data.workflow_id)
            if workflow_state:
                workflow_state.a1_submit_time = submit_time

            return task_record
            
        except Exception as e:
            self.logger.error(f"Failed to submit A1 task {task_data.task_id}: {e}")
            self.failed_submissions += 1
            
            # Return failed task record
            return TaskRecord(
                task_id=task_data.task_id,
                workflow_id=task_data.workflow_id,
                task_type="A1",
                sleep_time=task_data.sleep_time,
                exp_runtime=task_data.exp_runtime,
                submit_time=submit_time,
                status="failed",
                error=str(e),
                is_warmup=task_data.is_warmup
            )

    def _run(self):
        """Main submission loop (synchronous)."""
        self.logger.info(f"Starting A1 task submission (QPS={self.qps}, num_tasks={len(self.tasks)})")
        
        # Generate inter-arrival times using exponential distribution
        intervals = np.random.exponential(1.0 / self.qps, len(self.tasks))
        
        self.submission_start_time = time.time()
        
        for task_data, wait_time in zip(self.tasks, intervals):
            if not self.running:
                break
            
            # Apply global rate limiting if configured
            if self.rate_limiter:
                self.rate_limiter.acquire()
            
            # Submit task
            task_record = self._submit_task(task_data)
            self.submitted_tasks.append(task_record)
            
            # Wait for next submission
            time.sleep(wait_time)
        
        self.submission_end_time = time.time()
        total_time = self.submission_end_time - self.submission_start_time
        actual_qps = len(self.tasks) / total_time if total_time > 0 else 0
        
        self.logger.info(f"A1 task submission completed: {len(self.submitted_tasks)} tasks in {total_time:.2f}s (actual QPS: {actual_qps:.2f})")
        if self.failed_submissions > 0:
            self.logger.warning(f"Failed to submit {self.failed_submissions} A1 tasks")

    def start(self):
        """Start the submission thread."""
        if self.running:
            self.logger.warning("A1 task submitter already running")
            return
        
        self.running = True
        self.thread = threading.Thread(target=self._run, daemon=True)
        self.thread.start()
        self.logger.info("A1 task submitter thread started")

    def stop(self):
        """Stop the submission thread."""
        if not self.running:
            return
        
        self.logger.info("Stopping A1 task submitter...")
        self.running = False

    def wait(self, timeout: Optional[float] = None):
        """Wait for submission thread to complete."""
        if self.thread:
            self.thread.join(timeout=timeout)


