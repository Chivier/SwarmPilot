@dataclass
class WorkflowState:
    """State tracking for a single workflow.

    Supports B task loops: A1→A2→B1→B2→B3→B4 (1-4 iterations).
    """
    workflow_id: str
    strategy: str
    a1_task_id: str
    a2_task_id: str

    # B task loop support (B can iterate 1-4 times)
    b_task_ids: List[str]  # List of B task IDs: ["task-B1-xxx", "task-B2-xxx", ...]
    b_submit_times: List[float] = field(default_factory=list)  # Submission times for each B iteration
    b_complete_times: List[float] = field(default_factory=list)  # Completion times for each B iteration
    b_loop_count: int = 0  # Current iteration (increments on each B task submission)
    max_b_loops: int = 1  # Maximum iterations allowed (1-4)

    is_warmup: bool = False
    is_target_for_stats: bool = True  # For continuous mode

    # A1/A2 timestamps (unchanged)
    a1_submit_time: Optional[float] = None
    a1_complete_time: Optional[float] = None
    a2_submit_time: Optional[float] = None
    a2_complete_time: Optional[float] = None
    workflow_complete_time: Optional[float] = None

    def is_workflow_complete(self) -> bool:
        """Check if workflow is complete (all B iterations done)."""
        return len(self.b_complete_times) > 0 and len(self.b_complete_times) >= self.max_b_loops

    def should_continue_b_loop(self) -> bool:
        """Check if we should submit another B task."""
        return self.b_loop_count < self.max_b_loops

    def mark_a1_complete(self, complete_time: float):
        """Mark A1 task as complete."""
        self.a1_complete_time = complete_time

    def mark_a2_complete(self, complete_time: float):
        """Mark A2 task as complete."""
        self.a2_complete_time = complete_time

    def mark_b_iteration_complete(self, complete_time: float):
        """Mark current B iteration as complete."""
        self.b_complete_times.append(complete_time)

    def prepare_next_b_iteration(self) -> bool:
        """Prepare for next B iteration. Returns True if should continue."""
        if self.should_continue_b_loop():
            self.b_loop_count += 1
            return True
        return False

    def mark_workflow_complete(self, complete_time: float):
        """Finalize workflow completion."""
        self.workflow_complete_time = complete_time

    # Backward compatibility properties
    @property
    def b_task_id(self) -> Optional[str]:
        """Get first B task ID (backward compatibility)."""
        return self.b_task_ids[0] if self.b_task_ids else None

    @property
    def b_submit_time(self) -> Optional[float]:
        """Get first B submit time (backward compatibility)."""
        return self.b_submit_times[0] if self.b_submit_times else None

    @property
    def b_complete_time(self) -> Optional[float]:
        """Get last B complete time (backward compatibility)."""
        return self.b_complete_times[-1] if self.b_complete_times else None


