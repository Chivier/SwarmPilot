@dataclass
class WorkflowTaskData:
    """Pre-generated task data for workflow execution."""
    task_id: str
    workflow_id: str
    task_type: str  # "A1", "A2", or "B"
    sleep_time: float
    exp_runtime: float  # Expected runtime in milliseconds
