class BTaskReceiver:
    """
    Thread 4: Receive B results from Scheduler B and mark workflows complete.
    
    When a B task completes, the entire workflow is complete.
    """

    def __init__(self,
                 ws_url: str,
                 b_task_ids: List[str],
                 workflow_states: Dict[str, WorkflowState],
                 task_records: Dict[str, TaskRecord],
                 completion_queue: Queue):
        """
        Initialize B task receiver.

        Args:
            ws_url: WebSocket URL for Scheduler B
            b_task_ids: List of B task IDs to subscribe to
            workflow_states: Shared workflow state dictionary
            task_records: Shared task record dictionary
            completion_queue: Queue for workflow completion events
        """
        self.ws_url = ws_url
        self.b_task_ids = b_task_ids
        self.workflow_states = workflow_states
        self.task_records = task_records
        self.completion_queue = completion_queue
        self.logger = logging.getLogger("Thread4.BTaskReceiver")

        # Tracking
        self.completed_b_tasks = 0
        self.failed_b_tasks = 0

        # Thread control
        self.thread: Optional[threading.Thread] = None
        self.running = False
        self.loop: Optional[asyncio.AbstractEventLoop] = None

    async def _run_async(self):
        """Main async loop for receiving B results."""
        self.logger.info(f"Connecting to Scheduler B WebSocket: {self.ws_url}")
        
        try:
            async with websockets.connect(self.ws_url) as websocket:
                # Subscribe to B task IDs
                subscribe_msg = {
                    "action": "subscribe",
                    "task_ids": self.b_task_ids
                }
                await websocket.send(json.dumps(subscribe_msg))
                self.logger.info(f"Subscribed to {len(self.b_task_ids)} B tasks")
                
                # Receive loop
                while self.running:
                    try:
                        message = await asyncio.wait_for(websocket.recv(), timeout=1.0)
                        data = json.loads(message)
                        
                        # Process B task completion
                        await self._handle_b_result(data)
                        
                    except asyncio.TimeoutError:
                        continue
                    except websockets.ConnectionClosed:
                        self.logger.warning("WebSocket connection closed")
                        break
                    except Exception as e:
                        self.logger.error(f"Error receiving B result: {e}")
                        
        except Exception as e:
            self.logger.error(f"WebSocket connection error: {e}")

    async def _handle_b_result(self, data: Dict):
        """Handle B task completion and mark workflow complete."""
        task_id = data.get("task_id")
        workflow_id = data.get("workflow_id")
        status = data.get("status")
        result = data.get("result", {})
        execution_time_ms = data.get("execution_time_ms")
        
        complete_time = time.time()
        
        # Update task record
        if task_id in self.task_records:
            task_record = self.task_records[task_id]
            task_record.complete_time = complete_time
            task_record.status = status
            task_record.execution_time_ms = execution_time_ms
            task_record.result = result
        
        # Update workflow state
        workflow_state = self.workflow_states.get(workflow_id)
        if not workflow_state:
            self.logger.error(f"Workflow state not found for {workflow_id}")
            return
        
        # Check task status
        if status != "completed":
            self.logger.warning(f"B task {task_id} failed with status: {status}")
            self.failed_b_tasks += 1
            return
        
        # Mark B as complete (also marks workflow as complete)
        workflow_state.mark_b_complete(complete_time)
        self.completed_b_tasks += 1
        
        # Create workflow completion event
        workflow_time = complete_time - workflow_state.a1_submit_time
        completion_event = WorkflowCompletionEvent(
            workflow_id=workflow_id,
            workflow_time=workflow_time,
            a1_task_id=workflow_state.a1_task_id,
            a2_task_id=workflow_state.a2_task_id,
            b_task_id=workflow_state.b_task_id,
            a1_submit_time=workflow_state.a1_submit_time,
            a1_complete_time=workflow_state.a1_complete_time,
            a2_submit_time=workflow_state.a2_submit_time,
            a2_complete_time=workflow_state.a2_complete_time,
            b_submit_time=workflow_state.b_submit_time,
            b_complete_time=workflow_state.b_complete_time,
            workflow_complete_time=workflow_state.workflow_complete_time,
            is_warmup=workflow_state.is_warmup,
            is_target_for_stats=workflow_state.is_target_for_stats
        )
        
        # Push to completion queue
        self.completion_queue.put(completion_event)
        
        self.logger.info(f"Workflow {workflow_id} completed in {workflow_time:.2f}s")

    def _run(self):
        """Thread entry point."""
        self.loop = asyncio.new_event_loop()
        asyncio.set_event_loop(self.loop)
        self.loop.run_until_complete(self._run_async())

    def start(self):
        """Start the receiver thread."""
        if self.running:
            self.logger.warning("B task receiver already running")
            return
        
        self.running = True
        self.thread = threading.Thread(target=self._run, daemon=True)
        self.thread.start()
        self.logger.info("B task receiver thread started")

    def stop(self):
        """Stop the receiver thread."""
        if not self.running:
            return
        
        self.logger.info("Stopping B task receiver...")
        self.running = False

    def wait(self, timeout: Optional[float] = None):
        """Wait for receiver thread to complete."""
        if self.thread:
            self.thread.join(timeout=timeout)


