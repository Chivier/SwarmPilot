class A1TaskReceiver:
    """
    Thread 2: Receive A1 results from Scheduler A and submit A2 tasks.
    
    When an A1 task completes:
    1. Extract positive prompt from result
    2. Submit corresponding A2 task to Scheduler A
    """

    def __init__(self,
                 scheduler_url: str,
                 ws_url: str,
                 a1_task_ids: List[str],
                 workflow_states: Dict[str, WorkflowState],
                 task_records: Dict[str, TaskRecord],
                 a2_tasks: Dict[str, WorkflowTaskData],
                 model_id: str = "llm_service_small_model",
                 mode: str = "simulation",
                 rate_limiter: Optional[RateLimiter] = None):
        """
        Initialize A1 task receiver.

        Args:
            scheduler_url: Scheduler A URL
            ws_url: WebSocket URL for Scheduler A
            a1_task_ids: List of A1 task IDs to subscribe to
            workflow_states: Shared workflow state dictionary
            task_records: Shared task record dictionary
            a2_tasks: Pre-generated A2 task data (keyed by workflow_id)
            model_id: Model ID for A2 tasks
            mode: "simulation" or "real"
            rate_limiter: Optional shared rate limiter
        """
        self.scheduler_url = scheduler_url
        self.ws_url = ws_url
        self.a1_task_ids = a1_task_ids
        self.workflow_states = workflow_states
        self.task_records = task_records
        self.a2_tasks = a2_tasks
        self.model_id = model_id
        self.mode = mode
        self.rate_limiter = rate_limiter
        self.logger = logging.getLogger("Thread2.A1TaskReceiver")

        # Tracking
        self.completed_a1_tasks = 0
        self.failed_a1_tasks = 0
        self.submitted_a2_tasks = 0
        self.failed_a2_submissions = 0

        # Thread control
        self.thread: Optional[threading.Thread] = None
        self.running = False
        self.loop: Optional[asyncio.AbstractEventLoop] = None

    async def _run_async(self):
        """Main async loop for receiving A1 results."""
        self.logger.info(f"Connecting to Scheduler A WebSocket: {self.ws_url}")
        
        try:
            async with websockets.connect(self.ws_url) as websocket:
                # Subscribe to A1 task IDs
                subscribe_msg = {
                    "action": "subscribe",
                    "task_ids": self.a1_task_ids
                }
                await websocket.send(json.dumps(subscribe_msg))
                self.logger.info(f"Subscribed to {len(self.a1_task_ids)} A1 tasks")
                
                # Receive loop
                while self.running:
                    try:
                        message = await asyncio.wait_for(websocket.recv(), timeout=1.0)
                        data = json.loads(message)
                        
                        # Process A1 task completion
                        await self._handle_a1_result(data)
                        
                    except asyncio.TimeoutError:
                        continue
                    except websockets.ConnectionClosed:
                        self.logger.warning("WebSocket connection closed")
                        break
                    except Exception as e:
                        self.logger.error(f"Error receiving A1 result: {e}")
                        
        except Exception as e:
            self.logger.error(f"WebSocket connection error: {e}")

    async def _handle_a1_result(self, data: Dict):
        """Handle A1 task completion and submit A2 task."""
        task_id = data.get("task_id")
        workflow_id = data.get("workflow_id")
        status = data.get("status")
        result = data.get("result", {})
        execution_time_ms = data.get("execution_time_ms")
        
        complete_time = time.time()
        
        # Update task record
        if task_id in self.task_records:
            task_record = self.task_records[task_id]
            task_record.complete_time = complete_time
            task_record.status = status
            task_record.execution_time_ms = execution_time_ms
            task_record.result = result
        
        # Update workflow state
        workflow_state = self.workflow_states.get(workflow_id)
        if not workflow_state:
            self.logger.error(f"Workflow state not found for {workflow_id}")
            return
        
        # Check task status
        if status != "completed":
            self.logger.warning(f"A1 task {task_id} failed with status: {status}")
            self.failed_a1_tasks += 1
            return
        
        # Mark A1 as complete
        workflow_state.mark_a1_complete(complete_time)
        self.completed_a1_tasks += 1
        
        # Extract positive prompt from result
        if self.mode == "simulation":
            positive_prompt = result.get("output", "simulated positive prompt")
        else:
            positive_prompt = result.get("output", "")
        
        # Submit A2 task
        await self._submit_a2_task(workflow_id, positive_prompt)

    async def _submit_a2_task(self, workflow_id: str, positive_prompt: str):
        """Submit A2 task to Scheduler A."""
        a2_task_data = self.a2_tasks.get(workflow_id)
        if not a2_task_data:
            self.logger.error(f"A2 task data not found for workflow {workflow_id}")
            return
        
        # Apply global rate limiting if configured
        if self.rate_limiter:
            # Run in executor to avoid blocking async loop
            loop = asyncio.get_event_loop()
            await loop.run_in_executor(None, self.rate_limiter.acquire)
        
        # Build task_input based on mode
        if self.mode == "simulation":
            task_input = {"sleep_time": a2_task_data.sleep_time}
        else:  # real mode
            task_input = {
                "sentence": f"Generate negative prompt for: {positive_prompt}",
                "max_tokens": a2_task_data.max_tokens or 512
            }
        
        # Build metadata
        metadata = {
            "positive_prompt": positive_prompt,
            "task_type": "A2"
        }
        
        payload = {
            "task_id": a2_task_data.task_id,
            "model_id": self.model_id,
            "task_input": task_input,
            "metadata": metadata
        }
        
        # Set submit_time BEFORE attempting submission
        submit_time = time.time()
        
        try:
            # Submit via POST (run in executor to avoid blocking)
            loop = asyncio.get_event_loop()
            response = await loop.run_in_executor(
                None,
                lambda: requests.post(
                    f"{self.scheduler_url}/task/submit",
                    json=payload,
                    timeout=5.0
                )
            )
            response.raise_for_status()
            result = response.json()
            
            # Extract assigned_instance
            assigned_instance = result.get("assigned_instance")
            
            # Create task record
            task_record = TaskRecord(
                task_id=a2_task_data.task_id,
                workflow_id=workflow_id,
                task_type="A2",
                sleep_time=a2_task_data.sleep_time,
                exp_runtime=a2_task_data.exp_runtime,
                submit_time=submit_time,
                assigned_instance=assigned_instance,
                is_warmup=a2_task_data.is_warmup
            )
            
            # Store task record
            self.task_records[a2_task_data.task_id] = task_record
            
            # Update workflow state
            workflow_state = self.workflow_states.get(workflow_id)
            if workflow_state:
                workflow_state.a2_submit_time = submit_time
            
            self.submitted_a2_tasks += 1
            
        except Exception as e:
            self.logger.error(f"Failed to submit A2 task {a2_task_data.task_id}: {e}")
            self.failed_a2_submissions += 1

    def _run(self):
        """Thread entry point."""
        self.loop = asyncio.new_event_loop()
        asyncio.set_event_loop(self.loop)
        self.loop.run_until_complete(self._run_async())

    def start(self):
        """Start the receiver thread."""
        if self.running:
            self.logger.warning("A1 task receiver already running")
            return
        
        self.running = True
        self.thread = threading.Thread(target=self._run, daemon=True)
        self.thread.start()
        self.logger.info("A1 task receiver thread started")

    def stop(self):
        """Stop the receiver thread."""
        if not self.running:
            return
        
        self.logger.info("Stopping A1 task receiver...")
        self.running = False

    def wait(self, timeout: Optional[float] = None):
        """Wait for receiver thread to complete."""
        if self.thread:
            self.thread.join(timeout=timeout)


