def test_strategy_workflow(
    strategy: str,
    num_workflows: int,
    qps: float,
    workload: Text2VideoWorkload,
    mode: str = "simulation",
    model_a_id: str = "llm_service_small_model",
    model_b_id: str = "t2vid",
    gqps: Optional[float] = None,
    warmup_ratio: float = 0.0,
    continuous_mode: bool = False,
    timeout_minutes: int = 30
) -> Dict:
    """
    Test a single strategy with the given text2video workload.

    Args:
        strategy: Scheduling strategy name
        num_workflows: Number of workflows to execute
        qps: Target QPS for A1 task submission
        workload: Text2VideoWorkload with captions and frame counts
        mode: "simulation" or "real"
        model_a_id: Model ID for A1/A2 tasks
        model_b_id: Model ID for B tasks
        gqps: Optional global QPS limit
        warmup_ratio: Warmup workflow ratio (0.0-1.0)
        continuous_mode: Enable continuous request mode
        timeout_minutes: Test timeout in minutes

    Returns:
        Dictionary with test results and metrics
    """
    logger = logging.getLogger("TestWorkflow")
    logger.info(f"Starting test for strategy: {strategy}")
    logger.info(f"  Mode: {mode}")
    logger.info(f"  Workflows: {num_workflows}")
    logger.info(f"  QPS: {qps}")
    logger.info(f"  Global QPS: {gqps or 'None'}")
    logger.info(f"  Warmup ratio: {warmup_ratio}")
    logger.info(f"  Continuous mode: {continuous_mode}")

    # Step 1: Clear schedulers
    logger.info("Clearing schedulers...")
    from common import force_clear_scheduler_tasks
    force_clear_scheduler_tasks(SCHEDULER_A_URL)
    force_clear_scheduler_tasks(SCHEDULER_B_URL)
    time.sleep(2.0)  # Wait for cleanup

    # Step 2: Calculate number of workflows to generate
    num_warmup = int(num_workflows * warmup_ratio)
    if continuous_mode:
        # Continuous mode: generate 2x workflows, track middle num_workflows
        num_workflows_to_generate = num_workflows * 2 + num_warmup
    else:
        num_workflows_to_generate = num_workflows + num_warmup

    logger.info(f"Generating {num_workflows_to_generate} workflows ({num_warmup} warmup)")

    # Step 3: Generate task data for all workflows
    all_a1_tasks = []
    all_a2_tasks_dict = {}  # workflow_id -> A2 task data
    all_b_tasks_dict = {}   # workflow_id -> B task data
    workflow_states = {}

    for i in range(num_workflows_to_generate):
        workflow_id = f"{strategy}-workflow-{i:04d}"
        
        # Determine warmup and target status
        is_warmup = (i < num_warmup)
        if continuous_mode:
            is_target_for_stats = (i >= num_warmup) and (i < num_warmup + num_workflows)
        else:
            is_target_for_stats = not is_warmup

        # Get caption and frame count from workload
        caption = workload.captions[i % len(workload.captions)]
        frame_count = workload.frame_counts[i % len(workload.frame_counts)]

        # Task IDs
        a1_task_id = f"task-A1-{strategy}-workflow-{i:04d}"
        a2_task_id = f"task-A2-{strategy}-workflow-{i:04d}"
        b_task_id = f"task-B-{strategy}-workflow-{i:04d}"

        # For simulation mode, use sleep times
        if mode == "simulation":
            a1_sleep = 0.5  # Short sleep for LLM tasks
            a2_sleep = 0.5
            b_sleep = 10.0  # Longer sleep for video generation
        else:
            a1_sleep = 0.0
            a2_sleep = 0.0
            b_sleep = 0.0

        # Create A1 task data
        a1_task = WorkflowTaskData(
            task_id=a1_task_id,
            workflow_id=workflow_id,
            task_type="A1",
            sleep_time=a1_sleep,
            exp_runtime=500.0,  # Expected runtime in ms
            is_warmup=is_warmup,
            caption=caption,
            max_tokens=512
        )
        all_a1_tasks.append(a1_task)

        # Create A2 task data
        a2_task = WorkflowTaskData(
            task_id=a2_task_id,
            workflow_id=workflow_id,
            task_type="A2",
            sleep_time=a2_sleep,
            exp_runtime=500.0,
            is_warmup=is_warmup,
            max_tokens=512
        )
        all_a2_tasks_dict[workflow_id] = a2_task

        # Create B task data
        b_task = WorkflowTaskData(
            task_id=b_task_id,
            workflow_id=workflow_id,
            task_type="B",
            sleep_time=b_sleep,
            exp_runtime=10000.0,
            is_warmup=is_warmup,
            frame_count=frame_count
        )
        all_b_tasks_dict[workflow_id] = b_task

        # Create workflow state
        workflow_state = WorkflowState(
            workflow_id=workflow_id,
            strategy=strategy,
            a1_task_id=a1_task_id,
            a2_task_id=a2_task_id,
            b_task_id=b_task_id,
            is_warmup=is_warmup,
            is_target_for_stats=is_target_for_stats
        )
        workflow_states[workflow_id] = workflow_state

    logger.info(f"Generated {len(all_a1_tasks)} workflows")

    # Step 4: Create shared data structures
    task_records = {}  # task_id -> TaskRecord
    completion_queue = Queue()
    
    # Create rate limiter if global QPS specified
    rate_limiter = RateLimiter(gqps) if gqps else None
    if rate_limiter:
        logger.info(f"Created global rate limiter with {gqps} QPS")

    # Step 5: Create and start receiver threads
    logger.info("Starting receiver threads...")

    # Thread 2: A1 receiver (receives A1, submits A2)
    a1_task_ids = [task.task_id for task in all_a1_tasks]
    a1_receiver = A1TaskReceiver(
        scheduler_url=SCHEDULER_A_URL,
        ws_url=SCHEDULER_A_WS,
        a1_task_ids=a1_task_ids,
        workflow_states=workflow_states,
        task_records=task_records,
        a2_tasks=all_a2_tasks_dict,
        model_id=model_a_id,
        mode=mode,
        rate_limiter=rate_limiter
    )
    a1_receiver.start()

    # Thread 3: A2 receiver (receives A2, submits B)
    a2_task_ids = [task.task_id for task in all_a2_tasks_dict.values()]
    a2_receiver = A2TaskReceiver(
        scheduler_b_url=SCHEDULER_B_URL,
        ws_url=SCHEDULER_A_WS,
        a2_task_ids=a2_task_ids,
        workflow_states=workflow_states,
        task_records=task_records,
        b_tasks=all_b_tasks_dict,
        model_id=model_b_id,
        mode=mode,
        rate_limiter=rate_limiter
    )
    a2_receiver.start()

    # Thread 4: B receiver (receives B, marks workflow complete)
    b_task_ids = [task.task_id for task in all_b_tasks_dict.values()]
    b_receiver = BTaskReceiver(
        ws_url=SCHEDULER_B_WS,
        b_task_ids=b_task_ids,
        workflow_states=workflow_states,
        task_records=task_records,
        completion_queue=completion_queue
    )
    b_receiver.start()

    # Wait for WebSocket connections to establish
    logger.info("Waiting for WebSocket connections...")
    time.sleep(3.0)

    # Step 6: Start A1 task submitter (Thread 1)
    logger.info("Starting A1 task submitter...")
    a1_submitter = PoissonTaskSubmitter(
        scheduler_url=SCHEDULER_A_URL,
        tasks=all_a1_tasks,
        qps=qps,
        workflow_states=workflow_states,
        model_id=model_a_id,
        mode=mode,
        rate_limiter=rate_limiter
    )
    a1_submitter.start()

    # Step 7: Monitor workflow completion
    logger.info("Monitoring workflow completion...")
    start_time = time.time()
    timeout_seconds = timeout_minutes * 60
    completed_workflows = []

    while len(completed_workflows) < num_workflows_to_generate:
        # Check timeout
        elapsed = time.time() - start_time
        if elapsed > timeout_seconds:
            logger.error(f"Test timed out after {timeout_minutes} minutes")
            break

        # Try to get completion event
        try:
            event = completion_queue.get(timeout=1.0)
            completed_workflows.append(event)
            
            if len(completed_workflows) % 10 == 0:
                logger.info(f"Progress: {len(completed_workflows)}/{num_workflows_to_generate} workflows completed")
                
        except Empty:
            continue

    # Step 8: Stop all threads
    logger.info("Stopping threads...")
    a1_submitter.stop()
    a1_receiver.stop()
    a2_receiver.stop()
    b_receiver.stop()

    # Wait for threads to finish
    a1_submitter.wait(timeout=5.0)
    a1_receiver.wait(timeout=5.0)
    a2_receiver.wait(timeout=5.0)
    b_receiver.wait(timeout=5.0)

    # Step 9: Calculate metrics
    logger.info("Calculating metrics...")
    
    # Filter to target workflows for statistics
    target_workflows = [e for e in completed_workflows
                       if not e.is_warmup and e.is_target_for_stats]
    
    # Calculate makespan for continuous mode
    from common import calculate_makespan
    makespan_metrics = calculate_makespan(completed_workflows)

    # Calculate task-level metrics
    def calculate_task_metrics(task_type: str, target_only: bool = True):
        """Calculate metrics for a specific task type."""
        tasks = [r for r in task_records.values() if r.task_type == task_type]
        
        if target_only:
            # Filter to target workflows
            target_workflow_ids = [e.workflow_id for e in target_workflows]
            tasks = [t for t in tasks if t.workflow_id in target_workflow_ids]
        
        completed = [t for t in tasks if t.status == "completed" and t.complete_time]
        failed = [t for t in tasks if t.status != "completed" and t.status is not None]
        
        if not completed:
            return {
                "num_completed": 0,
                "num_failed": len(failed),
                "avg_completion_time": 0.0,
                "median_completion_time": 0.0,
                "p95_completion_time": 0.0,
                "p99_completion_time": 0.0,
                "min_completion_time": 0.0,
                "max_completion_time": 0.0
            }
        
        completion_times = [t.complete_time - t.submit_time for t in completed]
        
        return {
            "num_completed": len(completed),
            "num_failed": len(failed),
            "avg_completion_time": np.mean(completion_times),
            "median_completion_time": np.median(completion_times),
            "p95_completion_time": np.percentile(completion_times, 95),
            "p99_completion_time": np.percentile(completion_times, 99),
            "min_completion_time": np.min(completion_times),
            "max_completion_time": np.max(completion_times)
        }

    a1_metrics = calculate_task_metrics("A1")
    a2_metrics = calculate_task_metrics("A2")
    b_metrics = calculate_task_metrics("B")

    # Calculate workflow-level metrics
    if target_workflows:
        workflow_times = [e.workflow_time for e in target_workflows]
        wf_metrics = {
            "num_completed": len(target_workflows),
            "avg_workflow_time": np.mean(workflow_times),
            "median_workflow_time": np.median(workflow_times),
            "p95_workflow_time": np.percentile(workflow_times, 95),
            "p99_workflow_time": np.percentile(workflow_times, 99),
            "min_workflow_time": np.min(workflow_times),
            "max_workflow_time": np.max(workflow_times)
        }
    else:
        wf_metrics = {
            "num_completed": 0,
            "avg_workflow_time": 0.0,
            "median_workflow_time": 0.0,
            "p95_workflow_time": 0.0,
            "p99_workflow_time": 0.0,
            "min_workflow_time": 0.0,
            "max_workflow_time": 0.0
        }

    # Step 10: Print summary
    if continuous_mode:
        from common import print_continuous_mode_summary
        print_continuous_mode_summary(
            strategy=strategy,
            makespan_metrics=makespan_metrics,
            a1_metrics=a1_metrics,
            a2_metrics=a2_metrics,
            b_metrics=b_metrics,
            wf_metrics=wf_metrics
        )
    else:
        print("\n" + "=" * 80)
        print(f"Results: {strategy}")
        print("=" * 80)
        print(f"\nA1 Tasks: {a1_metrics['num_completed']} completed, {a1_metrics['num_failed']} failed")
        if a1_metrics['avg_completion_time'] > 0:
            print(f"  Avg: {a1_metrics['avg_completion_time']:.2f}s, P95: {a1_metrics['p95_completion_time']:.2f}s")
        
        print(f"\nA2 Tasks: {a2_metrics['num_completed']} completed, {a2_metrics['num_failed']} failed")
        if a2_metrics['avg_completion_time'] > 0:
            print(f"  Avg: {a2_metrics['avg_completion_time']:.2f}s, P95: {a2_metrics['p95_completion_time']:.2f}s")
        
        print(f"\nB Tasks: {b_metrics['num_completed']} completed, {b_metrics['num_failed']} failed")
        if b_metrics['avg_completion_time'] > 0:
            print(f"  Avg: {b_metrics['avg_completion_time']:.2f}s, P95: {b_metrics['p95_completion_time']:.2f}s")
        
        print(f"\nWorkflows: {wf_metrics['num_completed']} completed")
        if wf_metrics['avg_workflow_time'] > 0:
            print(f"  Avg: {wf_metrics['avg_workflow_time']:.2f}s")
            print(f"  Median: {wf_metrics['median_workflow_time']:.2f}s")
            print(f"  P95: {wf_metrics['p95_workflow_time']:.2f}s")
            print(f"  P99: {wf_metrics['p99_workflow_time']:.2f}s")
        print("=" * 80)

    # Step 11: Build results dictionary
    results = {
        "strategy": strategy,
        "mode": mode,
        "num_workflows": num_workflows,
        "qps": qps,
        "gqps": gqps,
        "warmup_ratio": warmup_ratio,
        "continuous_mode": continuous_mode,
        "a1_tasks": a1_metrics,
        "a2_tasks": a2_metrics,
        "b_tasks": b_metrics,
        "workflows": wf_metrics,
        "makespan": makespan_metrics if continuous_mode else None,
        "completed_workflows": [asdict(e) for e in completed_workflows],
        "task_records": {k: asdict(v) for k, v in task_records.items()}
    }

    return results


